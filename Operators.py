# ARITHMETIC OPERATOR
a=10
b=5
print(a+b) # Output: 15
print(a-b) # Output: 5
print(a*b) # Output: 50
print(a/b) # Output: 2.0
print(a//b) # Output: 2 (Floor Division)
print(a%b) # Output:0 (Modulus)
print(a**b) # Output: 100000 (Exponentiation)

# ASSIGNMENT OPERATOR
x=4 # Assigns 4 to x
x+=2 # Equivalent to x=x+2, now x is 6 
x-=9  # Equivalent to x=x-9, now x is -3
x*=7   # Equivalent to x=x+2, now x is -21
x/=4  # Equivalent to x=x+2, now x is -5.25

# COMPARISION OPERATOR
a=10
b=20
print(a==b) # Output: False
print(a>b) # Output: False
print(a<b) # Output: True
print(a!=b) # Output: True
print(a>=b) # Output: False
print(a<=b) # Output: True

# LOGICAL OPERATOR
# and operator
print(8>=8 and 9<7)  # Output: False (one of the condition is False)
# or operator
print(3>2 or 2>3) # Output: True (one of the condition True)
# not operator
print(not(5==5)) # Output: False (reverses True to False)

# MEMBERSHIP OPERATOR
name="anusha"
print("n" in name) # Output: True ("n" is in the string)
print("w" not in name) # Output: True ("w" is not in the string ) 

list=1,2,3,4,5
print(0 in list) # Output: False (0 is not in the list)
print(0 not in list) # Output: True (0 is not in the list)
 
# BITWISE OPERATOR
a=1 # In binary: 001
b=5 # In binary: 101
print(a&b) # Output: 1
print(a|b) # Output: 5
print(a^b) # Output: 4
print(a<<b) # Output: 32
print(a>>b) # Output: 0
print(a|b) # Output: 5
print(~a)  # Output: -2



